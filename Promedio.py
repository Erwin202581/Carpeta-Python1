"""Promedio de tres notas: Calcular el promedio de tres notas ingresadas por
el usuario
"""
print("Ingresa la primera nota")
Nota1 = float(input())
print("Ingrese la segunda nota")
Nota2 = float(input())
print("Ingrese la tercera nota")
Nota3 = float(input())
Promedio = (Nota1 + Nota2 + Nota3) / 3
print("El promedio de las notas es de: ", Promedio)
